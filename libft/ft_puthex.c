#include "libft.h"

void	ft_puthex(unsigned long long n)
{
	char	h[17];

	ft_strcpy(h, "0123456789ABCDEF");
	while (n >= 1)
	{
		ft_putchar(h[n % 16]);
		n = n / 16;
	}
}
