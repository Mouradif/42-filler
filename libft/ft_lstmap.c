#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*list;

	list = f(lst);
	if (lst->next)
		list->next = ft_lstmap(lst->next, f);
	return (list);
}
