#include <string.h>
#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i)
	{
		if (s[i] == (char)c)
			return ((char*)s + i);
		i--;
	}
	return ((*s == c) ? (char*)s : NULL);
}
