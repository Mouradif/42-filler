int	ft_strnchr(const char *s, int c)
{
	char	*str;
	int		n;

	n = 0;
	str = (char*)s;
	while (*str)
	{
		if (*str == (char)c)
			n++;
		str++;
	}
	if (*str == (char)c)
		n++;
	return (n);
}
